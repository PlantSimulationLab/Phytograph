// Capture submodule versions at build time and bake them into a generated TS
// module the app can import at runtime.
//
// Why this exists: PyHelios and helios-core are git submodules. Their versions
// live only in git metadata (submodule pointer + tags), which is NOT present in
// a packaged build (.git doesn't ship). So we resolve them here, at build/dev
// time, and write src/shared/generated/versionInfo.ts. The About dialog reads
// this — no manual bookkeeping, no drift when a submodule is bumped.
//
// App and backend versions are intentionally NOT captured here: they already
// live in package.json / constants.ts / main.py and move together under the
// version-lock contract (see CLAUDE.md). The About dialog reads those from
// their existing sources (app.getVersion() / EXPECTED_BACKEND_VERSION).
//
// Runs as a prebuild step for renderer/main and at the start of the dev loop.
// The output is gitignored — it's a build artifact, regenerated every time.

import { execFileSync } from 'node:child_process';
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..');
const pyheliosDir = join(repoRoot, 'pyhelios');
const heliosCoreDir = join(pyheliosDir, 'helios-core');
const outFile = join(repoRoot, 'src', 'shared', 'generated', 'versionInfo.ts');

// `git describe --tags` gives the nearest tag (e.g. "v0.1.22"), or
// "v1.3.74-3-gabc1234" if commits are ahead of the tag. Falls back to a short
// SHA, then to "unknown" if git isn't available or the submodule isn't checked
// out (e.g. a tarball build without submodules). Never throws — a missing
// version should degrade the About box, not break the build.
function describe(dir) {
  for (const args of [
    ['describe', '--tags', '--always'],
    ['rev-parse', '--short', 'HEAD'],
  ]) {
    try {
      const out = execFileSync('git', ['-C', dir, ...args], {
        encoding: 'utf-8',
        stdio: ['ignore', 'pipe', 'ignore'],
      }).trim();
      if (out) return out;
    } catch {
      // try the next strategy
    }
  }
  return 'unknown';
}

const pyheliosVersion = describe(pyheliosDir);
const heliosVersion = describe(heliosCoreDir);

const banner =
  '// AUTO-GENERATED by scripts/gen-version-info.mjs — do not edit.\n' +
  '// Submodule versions captured at build time. Gitignored; regenerated each build.\n';

const contents =
  banner +
  `export const PYHELIOS_VERSION = ${JSON.stringify(pyheliosVersion)};\n` +
  `export const HELIOS_CORE_VERSION = ${JSON.stringify(heliosVersion)};\n`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, contents, 'utf-8');

console.log(`[gen-version-info] pyhelios=${pyheliosVersion} helios-core=${heliosVersion}`);
